package Codes;

public class AbsoluteValue {
	// Write a java program to convert an integer to absolute value

	public static void main(String[] args) {
		int a = 356;
		int absA = Math.abs(a);
		System.out.println(absA);

		// Write a java program to convert an float to absolute value
		float b = -34515.97455f;
		float abdB = (b > 0) ? b : -b;
		System.out.println(abdB);

	}

}
